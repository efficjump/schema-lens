const messages: Record<Locale, Record<MessageKey, string>> = {
  en: englishMessages,
  ko: koreanMessages,
};

function interpolate(template: string, params: MessageParams = {}) {
  return template.replace(/\{(\w+)\}/g, (token, key: string) =>
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : token,
  );
}

export function translate(locale: Locale, key: MessageKey, params?: MessageParams) {
  return interpolate(messages[locale][key] ?? englishMessages[key], params);
}

function browserLocale(): Locale {
  const candidates = navigator.languages.length ? navigator.languages : [navigator.language];
  return candidates.some((candidate) => candidate.toLowerCase().startsWith("ko"))
    ? "ko"
    : "en";
}

type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: Translate;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");

  useEffect(() => {
    const stored = window.localStorage.getItem("schema-lens.locale");
    const initial = supportedLocales.includes(stored as Locale)
      ? (stored as Locale)
      : browserLocale();
    const frame = window.requestAnimationFrame(() => setLocaleState(initial));
    return () => window.cancelAnimationFrame(frame);
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => ({
    locale,
    setLocale(nextLocale) {
      setLocaleState(nextLocale);
      window.localStorage.setItem("schema-lens.locale", nextLocale);
    },
    t: (key, params) => translate(locale, key, params),
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error("useI18n must be used inside I18nProvider");
  return context;
}
